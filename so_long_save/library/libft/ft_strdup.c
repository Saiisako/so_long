#include "libft.h"

char	*ft_strdup(const char *s)
{
	char		*str;
	size_t		size;
	int			i;

	size = ft_strlen(s);
	str = (char *)malloc((size + 1) * sizeof(char));
	if (str == NULL)
		return (NULL);
	i = 0;
	while (s[i])
	{
		str[i] = s[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
